////////////////////////////////////////////////////////////////////////////////
class KeyboardButton : public Button
{
private:
	int k;
public:
	KeyboardButton(int _k);

	bool isDown();
};

////////////////////////////////////////////////////////////////////////////////
class JoystickButton : public Button
{
private:
	int pad, btn;
public:
	JoystickButton(int _pad, int _btn);

	bool isDown();
};

